def _platforms(code: str) -> list[str]:
    return PLATFORM_MAP[code]


def _mode(code: str) -> str:
    return MODE_MAP[code]


def _expand_category(cat: str, items: Iterable[tuple]) -> list[dict]:
    """Render one category's entries into the JSON shape."""
    out: list[dict] = []
    for idx, row in enumerate(items, start=1):
        (name, desc, cmd, attck, platform_code, mode_code, artifact, expected) = row
        ext_id = f"HIDS-{cat.upper().replace('_','')}-{idx:03d}"
        entry = {
            "payload_type": "HostAttack",
            "payload_name": f"{cat.replace('_',' ').title()}: {name}",
            "payload_external_id": ext_id,
            "payload_source": "COMMUNITY",
            "payload_status": "VERIFIED",
            "payload_description": desc,
            "payload_platforms": _platforms(platform_code),
            "payload_execution_arch": "ALL_ARCHITECTURES",
            "payload_expectations": ["PREVENTION", "DETECTION"],
            "payload_attack_patterns": [attck] if attck else [],
            "payload_domains": [DOMAIN_TO_CLASSIFY],
            "payload_tags": ["hids", cat],
            "hids_category": cat,
            "hids_execution_mode": _mode(mode_code),
            "hids_command_template": cmd,
            "hids_artifact_path": artifact,
            "hids_expected_artifacts": list(expected) if expected else [],
        }
        out.append(entry)
    return out


def main() -> None:
    total = 0
    for cat, items in ENTRIES.items():
        rendered = _expand_category(cat, items)
        path = OUT_DIR / f"{cat}.json"
        path.write_text(
            json.dumps(rendered, indent=2, ensure_ascii=False) + "\n",
            encoding="utf-8",
        )
        print(f"{cat:32s} {len(rendered):4d} entries → {path.name}")
        total += len(rendered)
    print(f"{'TOTAL':32s} {total:4d} entries across {len(ENTRIES)} categories")
    assert total >= 300, f"need ≥300 entries, got {total}"
    assert len(ENTRIES) == 12, f"need 12 categories, got {len(ENTRIES)}"


if __name__ == "__main__":
    main()
